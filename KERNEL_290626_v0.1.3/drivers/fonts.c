void font_init(void) {
    // Passa por todos os 256 índices ASCII estendidos
    for (int i = 0; i < 256; i++) {
        // Verifica se o caractere está completamente zerado (não mapeado)
        int vazio = 1;
        for (int j = 0; j < 8; j++) {
            if (font_8x8[i][j] != 0) {
                vazio = 0;
                break;
            }
        }
        // Se estiver zerado e não for o caractere de espaço ' ', aplica o fallback '?'
        if (vazio && i != ' ') {
            for (int j = 0; j < 8; j++) {
                font_8x8[i][j] = font_8x8['?'][j];
            }
        }
    }
}
